function toggle(){
    let value=document.getElementsByClassName("vertical-menubar")[0].style.display;
    if(value==="none")
    document.getElementsByClassName("vertical-menubar")[0].style.display="block";
    else document.getElementsByClassName("vertical-menubar")[0].style.display="none";
}
